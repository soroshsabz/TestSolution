namespace BSN.Resa.Core.Commons
{
    public enum SettableStatus
    {

        /// <summary>
        /// در فرم تسویه حساب گروهی در پنل امین 
        /// رکوردهایی را نمایش میدهد که قابل تسویه حساب میباشد
        /// </summary>
        Settable,

        /// <summary>
        /// در فرم تسویه حساب گروهی رکورد هایی را مشخص میکنند که صرفا جنبه نمایشی دارند
        /// و با رنگ زرد نمایش داده میشوند
        /// </summary>
        Unsettable
    }
}
